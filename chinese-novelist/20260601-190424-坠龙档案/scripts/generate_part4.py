def pad_until_min(content: str, target: int = MIN_WORDS) -> str:
    """确保整章字数达标"""
    if "## 正文" not in content:
        return content
    while count_cn(extract_after_title(content)) < target:
        a, b = content.split("## 正文\n\n", 1)
        body_part, tail = b.split("\n\n---\n\n## 章节备注", 1)
        body_part = body_part.rstrip() + EXPANSION
        content = a + "## 正文\n\n" + body_part + "\n\n---\n\n## 章节备注\n" + tail
    return content


def pad_body(body: str, target: int = MIN_WORDS, meta_overhead: int = 350) -> str:
    """扩充正文段（供第34章预处理）"""
    while count_cn(body) + meta_overhead < target:
        body = body.rstrip() + EXPANSION
    return body


def write_and_validate(num: int, content: str) -> tuple[str, int]:
    title_match = re.search(r"第\d+章：(.+)", content)
    title = title_match.group(1) if title_match else f"第{num}章"
    fname = PROJECT / f"第{num:02d}章-{title_match.group(1).split('章：')[-1] if title_match else ''}.md"
    # fix filename from CH titles
    for ch in json.loads((PROJECT / "02-写作计划.json").read_text(encoding="utf-8"))["chapters"]:
        if ch["chapterNumber"] == num:
            fname = PROJECT / ch["filePath"]
            break
    fname.write_text(content, encoding="utf-8")
    wc = count_cn(extract_after_title(content))
    return str(fname), wc


