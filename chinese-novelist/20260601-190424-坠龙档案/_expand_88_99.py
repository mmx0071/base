def dedupe_body(text: str) -> str:
    """Remove repeated paragraph blocks in 正文 section."""
    m = re.search(r"(## 正文\n\n)(.*?)(\n\n---\n\n## 章节备注)", text, re.DOTALL)
    if not m:
        return text
    head, body, tail = m.group(1), m.group(2), m.group(3)
    paras = [p.strip() for p in body.split("\n\n") if p.strip()]
    seen = []
    for p in paras:
        if p not in seen:
            seen.append(p)
    return text[: m.start()] + head + "\n\n".join(seen) + tail + text[m.end() :]


def expand_chapter(num: int):
    files = list(BASE.glob(f"第{num:02d}章-*.md"))
    if not files:
        print(f"Missing ch{num}")
        return
    path = files[0]
    text = path.read_text(encoding="utf-8")
    text = dedupe_body(text)
    exp = EXPANSIONS.get(num, "").strip()
    if exp:
        text = text.replace(
            "\n\n---\n\n## 章节备注",
            "\n" + exp + "\n\n---\n\n## 章节备注",
            1,
        )
    path.write_text(text, encoding="utf-8")
    r = subprocess.run(
        ["python3", str(CHECK), str(path)],
        capture_output=True,
        text=True,
    )
    for line in r.stdout.splitlines():
        if "字数" in line or "不足" in line or "达标" in line:
            print(line.strip())


def main():
    for n in range(88, 100):
        expand_chapter(n)


if __name__ == "__main__":
    main()
