def main():
    for n, text in PAD.items():
        files = list(BASE.glob(f"第{n:02d}章-*.md"))
        p = files[0]
        s = p.read_text(encoding="utf-8")
        if text.strip() not in s:
            s = s.replace("\n\n---\n\n## 章节备注", text + "\n\n---\n\n## 章节备注", 1)
        t2 = PAD2.get(n, "")
        if t2 and t2 not in s:
            s = s.replace("\n\n---\n\n## 章节备注", "\n\n" + t2 + "\n\n---\n\n## 章节备注", 1)
        p.write_text(s, encoding="utf-8")
        r = subprocess.run(["python3", str(CHECK), str(p)], capture_output=True, text=True)
        for line in r.stdout.splitlines():
            if "第" in line and "字数" in line:
                print(line.strip())

if __name__ == "__main__":
    main()
