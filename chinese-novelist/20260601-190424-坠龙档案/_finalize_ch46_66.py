def ensure_base():
    """若文件不存在则生成基础版"""
    gen = import_module('_gen_ch46_66')
    for item in gen.CHAPTERS:
        num, title, part, part_name, body = item
        if num in (45, 56):
            continue
        path = BASE / f"第{num:02d}章-{title}.md"
        if not path.exists():
            path.write_text(f"# 第{num}章：{title}\n\n{body}\n", encoding='utf-8')

def apply_addons(addons_dict):
    for num, addon in addons_dict.items():
        files = list(BASE.glob(f"第{num:02d}章-*.md"))
        if not files:
            continue
        path = files[0]
        text = path.read_text(encoding='utf-8')
        lines = text.split('\n')
        title = lines[0]
        body_start = 1
        for i, line in enumerate(lines):
            if line.startswith('# 第') and '章' in line:
                body_start = i + 1
                break
        body = '\n'.join(lines[body_start:])
        marker = find_marker(body)
        body = insert_before_marker(body, addon, marker)
        path.write_text(title + '\n\n' + body, encoding='utf-8')

def update_json():
    json_path = BASE / "02-写作计划.json"
    data = json.loads(json_path.read_text(encoding='utf-8'))
    tz = timezone(timedelta(hours=8))
    for ch in data['chapters']:
        n = ch['chapterNumber']
        if n in (45, 56) or n < 46 or n > 66:
            continue
        files = list(BASE.glob(f"第{n:02d}章-*.md"))
        if not files:
            continue
        text = files[0].read_text(encoding='utf-8')
        body = text.split('\n', 1)[1] if '\n' in text else ''
        wc = count(body)
        ch['status'] = 'completed'
        ch['wordCount'] = wc
        ch['wordCountPass'] = wc >= MIN
        ch['needsExpansion'] = wc < MIN
        if wc < MIN:
            ch['retryCount'] = ch.get('retryCount', 0) + 1
    data['updatedAt'] = datetime.now(tz).strftime('%Y-%m-%dT%H:%M:%S+08:00')
    json_path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + '\n', encoding='utf-8')

