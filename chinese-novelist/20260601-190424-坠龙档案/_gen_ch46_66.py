def count_chinese_words(text: str) -> int:
    text = re.sub(r'#{1,6}\s*', '', text)
    chinese_chars = re.findall(r'[\u4e00-\u9fff]', text)
    return len(chinese_chars)


def make_chapter(num, title, body):
    return f"# 第{num}章：{title}\n\n{body}\n"


def write_chapters():
    results = []
    for num, title, part, part_name, body in CHAPTERS:
        if num in (45, 56, 57, 58, 59, 60, 61):
            continue
        content = make_chapter(num, title, body)
        fname = BASE / f"第{num:02d}章-{title}.md"
        fname.write_text(content, encoding='utf-8')
        wc = count_chinese_words(body)
        status = 'pass' if wc >= MIN_WORDS else 'fail'
        results.append((num, title, wc, status, fname.name))
        print(f"  {fname.name}: {wc} 字 [{status}]")
    return results


def update_json(results):
    json_path = BASE / "02-写作计划.json"
    data = json.loads(json_path.read_text(encoding='utf-8'))
    result_map = {r[0]: r for r in results}
    for ch in data['chapters']:
        n = ch['chapterNumber']
        if n in result_map:
            _, title, wc, status, fname = result_map[n]
            ch['status'] = 'completed'
            ch['wordCount'] = wc
            ch['wordCountPass'] = status == 'pass'
            ch['needsExpansion'] = status == 'fail'
            if status == 'fail':
                ch['retryCount'] = ch.get('retryCount', 0) + 1
    from datetime import datetime, timezone, timedelta
    tz = timezone(timedelta(hours=8))
    data['updatedAt'] = datetime.now(tz).strftime('%Y-%m-%dT%H:%M:%S+08:00')
    json_path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + '\n', encoding='utf-8')
    print(f"\nUpdated {json_path.name}")


